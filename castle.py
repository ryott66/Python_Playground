import itertools
def haiti(dt:list):
  while True:
    n=int(input("見張りの人数を入力してください\n："))
    if 1<n<len(dt):
      break
    print("エラー\n")
  num=[]
  for i in range(0,len(dt)): 
    num.append(i)
  comb=list(itertools.combinations(num,n))
  sum=[0]*n
  ans=0

  for i in comb:
  #sum作成
    for x in range(0,n-1):
      for j in range(i[x],i[x+1]):
        sum[x]+=dt[j]
    #sum[n]だけは特別
    for k in range(i[n-1],len(dt)):
      sum[n-1]+=dt[k]
    for l in range(0,i[0]):
      sum[n-1]+=dt[l]
    #sumの最小が最大になってるか
    if min(sum)>ans:
      ans=min(sum)
      ansnum=i
    sum=[0]*n  #sumをリセット

  print("見張りを配置する塔は")
  for i in range(0,n):
   print("{:d}番目".format(ansnum[i]+1),end=" ")
  print("この時の塔間の距離の最小値は{:d}です。\n".format(ans))

